private void  DisplayPassengers()
{try{
    Class.forName("com.mysql.cj.jdbc.Driver");
    con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?zeroDateTimeBehavior=CONVERT_TO_NULL","root","root");
    st=con.createStatement();
    rs=st.executeQuery("select * from passengerstbl");
    DefaultTableModel tm=new DefaultTableModel();
    tm.setColumnIdentifiers(new Object[]{"Pid","PName","PNat","PGen","PPass","PAdd","Pphone"});
    while(rs.next())
    {
        tm.addRow(new Object[]{
        rs.getString("Pid"),
            rs.getString("PName"),
            rs.getString("PNat"),
            rs.getString("PGen"),
            rs.getString("PPass"),
            rs.getString("PAdd"),
            rs.getString("Pphone")
        });
    }
    Passengertable.setModel(tm);
    
}
catch(Exception e)
{
    JOptionPane.showMessageDialog(this,e);
}
    
}
