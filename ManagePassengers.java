    private void SavebuttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SavebuttonMouseClicked
        if(Pnametb.getText().isEmpty()|| Passnumtb.getText().isEmpty()|| Addresstb.getText().isEmpty()||Phonenumtb.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Missing Information");
        }
        else
        {
          try{
              countpass();
              Class.forName("com.mysql.cj.jdbc.Driver");
              con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/airline?zeroDateTimeBehavior=CONVERT_TO_NULL","root","root");
             PreparedStatement Add=null;
             Add = con.prepareStatement("insert into passengerstbl (Pid,PName,PNat,PGen,PPass,PAdd,Pphone)values(?,?,?,?,?,?,?)");
              Add.setInt(1,passid);
              Add.setString(2,Pnametb.getText());
              Add.setString(3,Nationtb.getText());
              Add.setString(4,Gendercombobox.getSelectedItem().toString());
              Add.setString(5,Passnumtb.getText());
              Add.setString(6,Addresstb.getText());
              Add.setString(7,Phonenumtb.getText());
              int row=Add.executeUpdate();
              JOptionPane.showMessageDialog(this,"Passenger Added");
              con.close();
              DisplayPassengers();
              clear();
          }  
          catch(Exception e)
          {
              JOptionPane.showMessageDialog(this,e);
          }
        }
    }//GEN-LAST:event_SavebuttonMouseClicked

